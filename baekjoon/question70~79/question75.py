# 75. 소인수분해
# https://www.acmicpc.net/problem/11653

# 12 -> 12 / 2 = 6 ... 6 / 2 = 3 ... 3 / 2 = 1 1 ... 3 / 3 = 1 ... 1
# 16 -> 16 / 2 = 8 ... 8 / 2 = 4 ... 4 / 2 = 2 ... 2 / 2 = 1 ... 1
# 20 -> 20 / 2 = 10 ... 10 / 2 = 5 ... 5 / 2 = 2 1 ... 5 / 3 = 1 2 ... 5 / 4 = 1 1 ... 5 / 5 = 1 ... 1
# 몫이 1이 되면 반복문에서 아웃되게 해야함.

N = int(input())
div_num = 2

while N != 1:
    if N % div_num == 0:
        print(div_num)
        N = N // div_num
    else:
        div_num += 1